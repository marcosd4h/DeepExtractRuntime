class TestRpcParameter:
    """Unit tests for RpcParameter risk scoring and to_dict."""

    def test_risk_score_string(self):
        p = RpcParameter(name="p0", ndr_type="string", direction="in")
        assert p.risk_score == pytest.approx(0.8, abs=0.05)

    def test_risk_score_byte_array(self):
        p = RpcParameter(name="buf", ndr_type="byte[]", direction="in", is_array=True)
        assert p.risk_score == pytest.approx(0.9, abs=0.05)

    def test_risk_score_int(self):
        p = RpcParameter(name="flags", ndr_type="int", direction="in")
        assert p.risk_score == pytest.approx(0.3, abs=0.05)

    def test_risk_score_context_handle(self):
        p = RpcParameter(name="h", ndr_type="NdrContextHandle", direction="in")
        assert p.risk_score == pytest.approx(0.2, abs=0.05)

    def test_risk_score_struct(self):
        p = RpcParameter(name="s", ndr_type="Struct_0", direction="in")
        assert p.risk_score == pytest.approx(0.6, abs=0.05)

    def test_to_dict(self):
        p = RpcParameter(
            name="data",
            ndr_type="byte[]",
            direction="in",
            is_array=True,
            is_pointer=False,
        )
        d = p.to_dict()
        assert d["name"] == "data"
        assert d["ndr_type"] == "byte[]"
        assert d["direction"] == "in"
        assert d["is_array"] is True
        assert d["is_pointer"] is False
        assert "risk_score" in d



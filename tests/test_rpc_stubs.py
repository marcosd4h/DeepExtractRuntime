_STUBS_PRESENT = _STUBS_DIR.exists()


@pytest.mark.skipif(not _STUBS_PRESENT, reason="stub files not present")
class TestStubFileLive:
    """Integration tests against real NtApiDotNet-generated stubs."""

    def test_parse_eventlog_stub(self):
        path = _STUBS_DIR / "f6beaff7-1e19-4fbb-9f8f-b89e2018337c_1.0.cs"
        stub = parse_stub_file(path)
        assert stub is not None
        assert stub.interface_id == "f6beaff7-1e19-4fbb-9f8f-b89e2018337c"
        assert "wevtsvc.dll" in stub.source_executable

        names = [p.name for p in stub.procedures]
        evt_procs = [n for n in names if n.startswith("EvtRpc_s_")]
        assert len(evt_procs) >= 5
        assert "EvtRpc_s_RegisterRemoteSubscription" in names
        assert "EvtRpc_s_ClearLog" in names

    def test_parse_rprn_stub(self):
        path = _STUBS_DIR / "12345678-1234-abcd-ef00-0123456789ab_1.0.cs"
        stub = parse_stub_file(path)
        assert stub is not None
        assert stub.interface_id == "12345678-1234-abcd-ef00-0123456789ab"
        assert "spoolsv" in stub.source_executable.lower()

        names = [p.name for p in stub.procedures]
        assert "RpcEnumPrinters" in names
        assert "RpcOpenPrinter" in names

    def test_parse_scmr_stub(self):
        path = _STUBS_DIR / "367abb81-9844-35f1-ad32-98f038001003_2.0.cs"
        stub = parse_stub_file(path)
        assert stub is not None
        assert stub.interface_id == "367abb81-9844-35f1-ad32-98f038001003"
        assert "services.exe" in stub.source_executable.lower()

        names = [p.name for p in stub.procedures]
        assert "RCloseServiceHandle" in names
        assert "ROpenSCManagerW" in names
        assert "RChangeServiceConfigW" in names

    def test_load_stubs_from_directory(self):
        stubs = load_stubs_from_directory(_STUBS_DIR)
        assert len(stubs) >= 400

        assert "f6beaff7-1e19-4fbb-9f8f-b89e2018337c" in stubs
        assert "12345678-1234-abcd-ef00-0123456789ab" in stubs
        assert "367abb81-9844-35f1-ad32-98f038001003" in stubs

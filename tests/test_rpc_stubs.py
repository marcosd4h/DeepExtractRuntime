_MOCK_STUB_CS = textwrap.dedent("""\
    //------------------------------------------------------------------------------
    // <auto-generated>
    //     This code was generated by a tool.
    // </auto-generated>
    //------------------------------------------------------------------------------

    // Source Executable: c:\\windows\\system32\\testsvc.dll
    // Interface ID: aaaaaaaa-bbbb-cccc-dddd-eeeeeeeeeeee
    // Interface Version: 1.0

    namespace rpc_test
    {
        #region Marshal Helpers
        public struct Struct_0 { public int field0; }
        #endregion

        public sealed class Client : NtApiDotNet.Win32.Rpc.RpcClientBase
        {
            public Client() { }

            public uint DoFoo(string p0, int p1, out int p2)
            {
                // stub
            }

            public uint DoBar(byte[] p0, ref string p1, out Struct_0 p2)
            {
                // stub
            }

            public uint DoArrays(byte[] p0, int[] p1, string[] p2)
            {
                // stub
            }
        }
    }
""")


class TestStubFileParsing:
    """Parse synthetic .cs stub content via temp files."""

    @pytest.fixture()
    def stub_path(self, tmp_path: Path) -> Path:
        p = tmp_path / "aaaaaaaa-bbbb-cccc-dddd-eeeeeeeeeeee_1.0.cs"
        p.write_text(_MOCK_STUB_CS, encoding="utf-8")
        return p

    def test_parse_header(self, stub_path: Path):
        stub = parse_stub_file(stub_path)
        assert stub is not None
        assert stub.interface_id == "aaaaaaaa-bbbb-cccc-dddd-eeeeeeeeeeee"
        assert stub.interface_version == "1.0"
        assert "testsvc.dll" in stub.source_executable

    def test_parse_methods(self, stub_path: Path):
        stub = parse_stub_file(stub_path)
        assert stub is not None
        names = [p.name for p in stub.procedures]
        assert "DoFoo" in names
        assert "DoBar" in names

        do_foo = stub.get_procedure("DoFoo")
        assert do_foo is not None
        assert do_foo.return_type == "uint"
        type_names = [p.ndr_type for p in do_foo.parameters]
        assert "string" in type_names
        assert "int" in type_names

    def test_parse_out_params(self, stub_path: Path):
        stub = parse_stub_file(stub_path)
        assert stub is not None
        do_foo = stub.get_procedure("DoFoo")
        assert do_foo is not None
        out_params = [p for p in do_foo.parameters if p.direction == "out"]
        assert len(out_params) == 1
        assert out_params[0].name == "p2"

    def test_parse_ref_params(self, stub_path: Path):
        stub = parse_stub_file(stub_path)
        assert stub is not None
        do_bar = stub.get_procedure("DoBar")
        assert do_bar is not None
        inout_params = [p for p in do_bar.parameters if p.direction == "inout"]
        assert len(inout_params) == 1
        assert inout_params[0].name == "p1"

    def test_parse_array_types(self, stub_path: Path):
        stub = parse_stub_file(stub_path)
        assert stub is not None
        do_arrays = stub.get_procedure("DoArrays")
        assert do_arrays is not None
        for param in do_arrays.parameters:
            assert param.is_array is True, f"{param.name} ({param.ndr_type}) should be is_array=True"



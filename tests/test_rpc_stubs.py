class TestRpcProcedureSignature:
    """Unit tests for RpcProcedureSignature computed properties."""

    @staticmethod
    def _make_sig(params: list[RpcParameter]) -> RpcProcedureSignature:
        return RpcProcedureSignature(name="TestProc", opnum=0, parameters=params)

    def test_input_count(self):
        params = [
            RpcParameter(name="a", ndr_type="int", direction="in"),
            RpcParameter(name="b", ndr_type="string", direction="in"),
            RpcParameter(name="c", ndr_type="int", direction="in"),
            RpcParameter(name="d", ndr_type="int", direction="out"),
            RpcParameter(name="e", ndr_type="int", direction="out"),
        ]
        sig = self._make_sig(params)
        assert sig.input_parameter_count == 3

    def test_has_string_inputs(self):
        params = [
            RpcParameter(name="name", ndr_type="string", direction="in"),
            RpcParameter(name="flags", ndr_type="int", direction="in"),
        ]
        sig = self._make_sig(params)
        assert sig.has_string_inputs is True

    def test_has_byte_buffer_inputs(self):
        params = [
            RpcParameter(name="buf", ndr_type="byte[]", direction="in", is_array=True),
        ]
        sig = self._make_sig(params)
        assert sig.has_byte_buffer_inputs is True

    def test_max_input_risk(self):
        params = [
            RpcParameter(name="flags", ndr_type="int", direction="in"),
            RpcParameter(name="name", ndr_type="string", direction="in"),
            RpcParameter(name="out_buf", ndr_type="byte[]", direction="out", is_array=True),
        ]
        sig = self._make_sig(params)
        assert sig.max_input_risk == pytest.approx(0.8, abs=0.05)


